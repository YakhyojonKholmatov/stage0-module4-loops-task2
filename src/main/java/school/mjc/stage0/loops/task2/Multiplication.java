public class Multiplication {
    public void printMultiplied(int multiplyByAndToInclusive) {
        int count=0;
        while (count<=(-1 *multiplyByAndToInclusive)){

                System.out.println(count*multiplyByAndToInclusive);

            ++count;


        }
    }
}
